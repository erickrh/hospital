    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        // TODO add your handling code here:
                String documento = jTextFieldIdentificacion.getText();
        if ("".equals(documento)) {
            JOptionPane.showMessageDialog(null, "Ingrese su cédula");
        }
        else {
            try {
                ConexionDB cone = new ConexionDB();
                cone.establecerconexion();
                Statement stam = cone.con.createStatement();
                ResultSet rtaset = stam.executeQuery("SELECT * FROM funcionario WHERE cedula='"+documento+"'");
                if (rtaset.next()) {
                    jTextFieldNombre.setText(rtaset.getString("nombres"));
                    jTextFieldApellidos.setText(rtaset.getString("apellidos"));
                    jTextFieldEdad.setText(rtaset.getString("edad"));
                    jTextFieldVacuna.setText(rtaset.getString("vacuna"));
                    jTextFieldCategoria.setText(rtaset.getString("categoria"));
                    jTextFieldCargo.setText(rtaset.getString("cargo"));
                } else {
                    JOptionPane.showMessageDialog(null, "Registro no encontrado");
                }
            }
            catch(SQLException ex) {
                Logger.getLogger(Consumer.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonConsultarActionPerformed

